export enum RecurrenceType {
  /** Every week on the same day of week (e.g. every Monday 5pm). */
  WEEKLY = 'WEEKLY',
  /** Every month on the same day of month (e.g. 25th of every month). */
  MONTHLY = 'MONTHLY',
  /** Every year on the same date (e.g. Jan 25th every year). */
  ANNUALLY = 'ANNUALLY',
}
